public class CustomProjects {

    private int id;
    private String name;
    private int issues;

    public CustomProjects(int id, String name, int issues) {
        this.id = id;
        this.name = name;
        this.issues = issues;
    }

    public int getId() {return id;}
    public String getName() {return name;}
    public int getIssues() {return issues;}

}
